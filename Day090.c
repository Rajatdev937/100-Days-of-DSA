// Problem Statement
// Given n boards of different lengths and k painters, each painter paints contiguous boards. Painting a unit length of board takes one unit of time.

// Determine the minimum time required to paint all boards.

// Input Format
// n k
// n space-separated integers representing board lengths

// Output Format
// Print the minimum time required to paint all boards.

// Sample Input
// 4 2
// 10 20 30 40

// Sample Output
// 60

// Explanation
// One painter paints boards of length 10, 20, and 30 (total 60), while the other paints board of length 40.
#include <stdio.h>

int isPossible(int arr[], int n, int k, int maxTime) {
    int painters = 1;
    int currSum = 0;

    for(int i = 0; i < n; i++) {

        // single board bigger than limit
        if(arr[i] > maxTime)
            return 0;

        if(currSum + arr[i] <= maxTime) {
            currSum += arr[i];
        }
        else {
            painters++;
            currSum = arr[i];

            if(painters > k)
                return 0;
        }
    }

    return 1;
}

int painterPartition(int arr[], int n, int k) {

    int low = arr[0];
    int high = 0;

    for(int i = 0; i < n; i++) {
        if(arr[i] > low)
            low = arr[i];

        high += arr[i];
    }

    int ans = high;

    while(low <= high) {

        int mid = low + (high - low) / 2;

        if(isPossible(arr, n, k, mid)) {
            ans = mid;
            high = mid - 1;
        }
        else {
            low = mid + 1;
        }
    }

    return ans;
}

int main() {

    int n, k;

    scanf("%d %d", &n, &k);

    int arr[n];

    for(int i = 0; i < n; i++) {
        scanf("%d", &arr[i]);
    }

    printf("%d", painterPartition(arr, n, k));

    return 0;
}